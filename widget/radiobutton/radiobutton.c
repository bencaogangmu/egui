static void radiobutton_force_state(struct radiobutton *c, si_t selected)
{
    c->selected = selected;
}

void radiobutton_select(struct radiobutton* c, union message *m)
{
    struct hashmap_key *key =
        hashmap_key_generate(c->group_name, strlen(c->group_name));
    struct list *res = NULL;
    if((res=hashmap_find(hm_groups, key, 0))) {
        struct list_node *data;
        data = list_find(res, radiobutton_list_find_focus);
        if(data) {
            struct radiobutton *focus;
            focus = *(struct radiobutton **)data->data;
            radiobutton_force_state(focus, 0);
            radiobutton_default_widget_repaint(focus, m);
            radiobutton_default_widget_show(focus, m);
        }
        radiobutton_force_state(c, 1);
        radiobutton_default_widget_repaint(c, m);
        radiobutton_default_widget_show(c, m);
    }
    free(key);
}
